import { addDataRow, addReportHeader, addSectionTitle, addTableHeaders, COLORS } from '../../../../../utils/ExportUtility'
import { createSheet, exportWorkbook, finalizeSheet, initExport } from '../ExportModule'

/** Column widths (in characters) are clamped to this readable range. */
const MIN_COLUMN_WIDTH = 10
const MAX_COLUMN_WIDTH = 42
const COLUMN_WIDTH_PADDING = 3
/** Data table starts at column 2, leaving column 1 as a slim gutter. */
const DATA_START_COL = 2

/** Formats a date value as "Mon DD, YYYY" for human-readable cells. */
function formatExportDate(value) {
    if (!value) return ''
    const date = new Date(value)
    if (isNaN(date)) return ''
    return date.toLocaleDateString('en-US', { day: 'numeric', month: 'short', year: 'numeric' })
}

/**
 * Converts a raw column value into an ExcelJS cell descriptor, applying the
 * per-type formatting (numbers with separators, dates humanized, booleans as
 * Yes/No, nulls left blank) and alignment.
 */
function renderCell(type, raw) {
    switch (type) {
        case 'number': {
            if (raw === null || raw === undefined || raw === '') return { align: 'right', value: '' }
            const numeric = Number(raw)
            return Number.isFinite(numeric)
                ? { align: 'right', format: '#,##0', value: numeric }
                : { align: 'right', value: '' }
        }
        case 'rating': {
            const numeric = Number(raw)
            return numeric > 0 ? { align: 'center', value: numeric } : { align: 'center', value: '' }
        }
        case 'date':
            return { align: 'center', value: formatExportDate(raw) }
        case 'bool':
            return { align: 'center', value: raw ? 'Yes' : 'No' }
        case 'text':
        default:
            return { align: 'left', value: raw === null || raw === undefined ? '' : String(raw) }
    }
}

/** Builds a descriptive, space-free filename: `<AssetType>-export-<YYYY-MM-DD>.xlsx`. */
function buildFilename(assetType) {
    const slug = assetType.trim().replace(/\s+/g, '-')
    return `${slug}-export-${new Date().toISOString().slice(0, 10)}.xlsx`
}

/**
 * Generates and downloads a clean, professional .xlsx of an asset type's
 * tracked data. Generic over asset type — the per-type column layout
 * (`{ header, type, get }[]`) and lookup `context` are supplied by the caller.
 *
 * @param {Object} params
 * @param {string} params.assetType - Singular label, e.g. "Mixer".
 * @param {string} [params.pluralLabel] - Plural label for the summary line.
 * @param {Array<Object>} params.rows - Asset rows to export (as the user sees them).
 * @param {Array<{header: string, type: string, get: Function}>} params.columns - Column layout.
 * @param {Object} [params.context] - Lookup context ({ plants, operators, tractors }).
 */
export async function exportAssetDataSheet({ assetType, pluralLabel, rows, columns, context = {} }) {
    if (!rows?.length || !columns?.length) return

    // Build the cell matrix once and measure each column's natural width.
    const measuredWidths = columns.map((col) => col.header.length)
    const dataMatrix = rows.map((item) =>
        columns.map((col, colIndex) => {
            const cell = renderCell(col.type, col.get(item, context))
            const length = String(cell.value ?? '').length
            if (length > measuredWidths[colIndex]) measuredWidths[colIndex] = length
            return cell
        })
    )

    const sheetTitle = `${assetType} Inventory`
    const { wb, logoBase64 } = await initExport({ subject: sheetTitle })
    const columnWidths = [
        { width: 3 },
        ...measuredWidths.map((width) => ({
            width: Math.min(Math.max(width + COLUMN_WIDTH_PADDING, MIN_COLUMN_WIDTH), MAX_COLUMN_WIDTH)
        }))
    ]
    const ws = createSheet(wb, assetType, { columns: columnWidths })

    const dateStr = new Date().toLocaleDateString('en-US', { day: 'numeric', month: 'long', year: 'numeric' })
    let row = addReportHeader(ws, wb, { logoBase64, subtitle: dateStr, title: sheetTitle })

    const plural = pluralLabel || `${assetType}s`
    addSectionTitle(ws, row, `${rows.length} ${rows.length === 1 ? assetType : plural}`)
    row += 2

    const headerRow = row
    addTableHeaders(ws, row, columns.map((col) => col.header), DATA_START_COL)
    row++

    dataMatrix.forEach((cells, index) => {
        addDataRow(ws, row, cells, DATA_START_COL, index % 2 === 1)
        row++
    })

    const lastCol = columns.length + 1
    row++
    ws.mergeCells(row, DATA_START_COL, row, Math.max(lastCol, DATA_START_COL + 1))
    const footerCell = ws.getCell(row, DATA_START_COL)
    footerCell.value = `Generated by Smyrna Tools on ${dateStr}`
    footerCell.font = { color: { argb: COLORS.slate500 }, italic: true, name: 'Calibri', size: 9 }
    footerCell.alignment = { horizontal: 'left', vertical: 'middle' }

    // Freeze the report band + table header so columns stay labeled while scrolling.
    ws.views = [{ showGridLines: false, state: 'frozen', xSplit: 0, ySplit: headerRow }]
    finalizeSheet(ws, { maxCol: lastCol + 1, maxRow: row + 5 })
    await exportWorkbook(wb, buildFilename(assetType))
}
